#include "libft.h"

int	ft_atoi(const char *str)
{
	int	str2;
	int	sign;

	str2 = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-')
		sign *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		str2 = str2 * 10 + *str - '0';
		str++;
	}
	return (str2 * sign);
}
